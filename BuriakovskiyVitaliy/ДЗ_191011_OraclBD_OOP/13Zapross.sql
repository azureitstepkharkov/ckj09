SELECT BVN.CAR.DRIVER_IN_CAR,
  BVN.CAR.CAR_STARTED,
  BVN.CARGO_CAR.CARRYING,
  BVN.CARGO_CAR.BODY_VOLUME,
  BVN.DUMP_TRUCK_CAR.ANGLE_RAISE_BODY,
  BVN.DUMP_TRUCK_CAR.BOARD_BACK
FROM BVN.CAR
INNER JOIN BVN.CARGO_CAR
ON BVN.CARGO_CAR.CAR_ID = BVN.CAR.ID
INNER JOIN BVN.PASSENGER_CAR
ON BVN.PASSENGER_CAR.CAR_ID = BVN.CAR.ID
INNER JOIN BVN.SPECIAL_CAR
ON BVN.SPECIAL_CAR.CAR_ID = BVN.CAR.ID
INNER JOIN BVN.DUMP_TRUCK_CAR
ON BVN.DUMP_TRUCK_CAR.CARGO_CAR_ID = BVN.CARGO_CAR.ID
INNER JOIN BVN.TANKER_CAR
ON BVN.TANKER_CAR.CARGO_CAR_ID = BVN.CARGO_CAR.ID
INNER JOIN BVN.WAGON_CAR
ON BVN.WAGON_CAR.ID = BVN.CARGO_CAR.ID
INNER JOIN BVN.ACCUMULATOR
ON BVN.CAR.ACCUMULATOR_ID = BVN.ACCUMULATOR.ID
INNER JOIN BVN.SECTION
ON BVN.ACCUMULATOR.SECTION_ID = BVN.SECTION.ID